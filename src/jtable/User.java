public Connection getConnection()
{
    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/user", "root", "");
        return con;
    } catch (SQLException ex) {
        Logger.getLogger(User.class.getName()).log(Level.SEVERE, null, ex);
        return null;
    }
}
    public ArrayList<Person> getUserList()
    {
        ArrayList<Person> userList = new ArrayList<>();
       Connection connection = getConnection();
        String query = "SELECT * FROM `user`.`user` ";
        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Person person;
            while (rs.next())
            {
                person = new Person(rs.getInt("id"), rs.getString("firstname"), rs.getString("lastname"), rs.getInt("age"));
                                userList.add(person);
            }
        } catch (Exception e)
        {
           e.printStackTrace(); 
        }
        return userList;
    }
    //Display data
    public void showUser()
   {
       ArrayList<Person> list = getUserList();
        model = (DefaultTableModel)jTable1.getModel();
       Object[] row = new Object[4];
       for(int i = 0; i < list.size(); i++)
       {
           row[0] = list.get(i).getId();
           row[1] = list.get(i).getFirstName();
           row[2] = list.get(i).getLastNAme();
           row[3] = list.get(i).getAge();
           
           model.addRow(row);
       }
    }
    // Execute The Insert Update And Delete 
   public void executeSQlQuery(String query, String message)
   {
        con = getConnection();
        
       try{
           st = con.createStatement();
           if((st.executeUpdate(query)) == 1)
           {
               // refresh jtable data
               DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
               model.setRowCount(0);
               showUser();
               
               JOptionPane.showMessageDialog(null, "Data "+message+" Succefully");
           }else{
               JOptionPane.showMessageDialog(null, "Data Not "+message);
           }
       }catch(SQLException | HeadlessException ex){
           ex.printStackTrace();
       }
   }
